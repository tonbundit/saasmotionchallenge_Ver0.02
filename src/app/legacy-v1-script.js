    function updateI18n() {
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        let text = TRANSLATIONS[curLang][key];
        if (text) {
          if (key === 'week_active') {
            const n = document.getElementById('pnchip')?.innerText.replace('WEEK ', '') || '01';
            text = text.replace('%n%', n);
          }
          el.innerText = text;
        }
      });
      // Tooltip/Placeholder updates
      const vInput = document.getElementById('m-video-url');
      if (vInput) vInput.placeholder = TRANSLATIONS[curLang].video_placeholder;
    }

